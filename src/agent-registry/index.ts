const AGENT_KEYS = Object.keys(AGENT_REGISTRY) as AgentId[];

/**
 * Fuzzy-match a free-form agent name string (e.g. "Claude Code", "Cursor",
 * "GitHub Copilot") to a canonical `AgentId`. Returns `null` when no match.
 *
 * Handles both hyphenated IDs ("claude-code") and spaced display names
 * ("Claude Code") since the backend persists the latter.
 */
export function resolveAgentId(name: string): AgentId | null {
  if (name.length < 2) return null;
  const lower = name.toLowerCase();
  // Exact match (hyphenated form)
  if (lower in AGENT_REGISTRY) return lower as AgentId;
  // Normalize spaces → hyphens so "Claude Code" matches "claude-code"
  const normalized = lower.replace(/\s+/g, "-");
  if (normalized in AGENT_REGISTRY) return normalized as AgentId;
  // Also strip spaces entirely so "VS Code" → "vscode" matches
  const stripped = lower.replace(/\s+/g, "");
  if (stripped in AGENT_REGISTRY) return stripped as AgentId;
  // Match against displayName (handles "VS Code" → "vscode" and other mismatches)
  for (const key of AGENT_KEYS) {
    if (AGENT_REGISTRY[key].displayName.toLowerCase() === lower) return key;
  }
  // Prefix / substring match against both forms
  for (const key of AGENT_KEYS) {
    if (lower.startsWith(key) || lower.includes(key) || key.startsWith(lower)) return key;
    // Also try space-separated form of the key ("claude-code" → "claude code")
    const spacedKey = key.replace(/-/g, " ");
    if (lower.startsWith(spacedKey) || lower.includes(spacedKey) || spacedKey.startsWith(lower)) return key;
  }
  return null;
}

