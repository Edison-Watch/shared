const FALLBACK_COLORS = [
  "#0891B2", // cyan-600
  "#7C3AED", // violet-600
  "#D97706", // amber-600
  "#E11D48", // rose-600
  "#059669", // emerald-600
  "#2563EB", // blue-600
  "#EA580C", // orange-600
  "#DB2777", // pink-600
];

/** Deterministic color for unknown agent names. */
export function agentFallbackColor(name: string): string {
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = (hash * 31 + name.charCodeAt(i)) | 0;
  }
  return FALLBACK_COLORS[Math.abs(hash) % FALLBACK_COLORS.length]!;
}

/** Two-letter monogram for unknown agents. */
export function agentMonogram(name: string): string {
  return name.slice(0, 2).toUpperCase();
}
